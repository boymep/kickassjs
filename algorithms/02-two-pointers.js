function assert(condition, message) {
  if (!condition) {
    console.error("❌ " + message);
  } else {
    console.log("✅ " + message);
  }
}

function assertDeepEqual(a, b, message) {
  assert(
    JSON.stringify(a) === JSON.stringify(b),
    message +
      " | got: " +
      JSON.stringify(a) +
      ", expected: " +
      JSON.stringify(b),
  );
}

// =====================================================
// * КАК РАБОТАЕТ ТЕХНИКА ДВУХ УКАЗАТЕЛЕЙ
// =====================================================
//
// ! Идея: вместо перебора всех пар O(n²) используем два указателя,
// ! которые движутся по массиву/строке по определённой логике.
//
// Три основных варианта:
//
// -----------------------------------------------------
// * 1. Сходящиеся указатели (навстречу друг другу):
// -----------------------------------------------------
//    left = 0, right = n-1. Движутся к центру.
//    Применение: задачи на отсортированных массивах,
//    палиндромы, поиск пар с заданным свойством.
//
//    Пример: найти два числа с суммой target = 9
//    в отсортированном массиве [1, 3, 5, 6, 8, 11].
//
//    Шаг 1: left=0, right=5 -> nums[0]+nums[5] = 1+11 = 12 > 9
//            Сумма слишком большая -> сдвигаем right влево.
//    Шаг 2: left=0, right=4 -> nums[0]+nums[4] = 1+8 = 9 == 9
//            Нашли! Ответ: [0, 4].
//
//    ? Почему это работает? Массив отсортирован. Если сумма
//    ? слишком большая — уменьшить её можно только сдвигом right
//    ? влево. Если слишком маленькая — только сдвигом left вправо.
//    ? Так мы гарантированно не пропустим ответ, проверив максимум
//    ? n пар вместо n*(n-1)/2.
//
// -----------------------------------------------------
// * 2. Параллельные указатели (в одном направлении):
// -----------------------------------------------------
//    slow и fast оба начинают с 0, но fast идёт быстрее.
//    Применение: удаление дубликатов in-place,
//    разделение массива по условию.
//
//    Пример: удалить дубликаты из [0, 0, 1, 1, 2, 3, 3].
//    slow — позиция для записи, fast — для чтения.
//
//    Начало:  slow=0, fast=1
//    Шаг 1:  fast=1, nums[1]=0 == nums[slow]=0 -> дубликат, пропускаем.
//    Шаг 2:  fast=2, nums[2]=1 != nums[slow]=0 -> новый элемент!
//            slow++ (slow=1), nums[1] = 1.   Массив: [0, 1, 1, 1, 2, 3, 3]
//    Шаг 3:  fast=3, nums[3]=1 == nums[slow]=1 -> дубликат, пропускаем.
//    Шаг 4:  fast=4, nums[4]=2 != nums[slow]=1 -> новый элемент!
//            slow++ (slow=2), nums[2] = 2.   Массив: [0, 1, 2, 1, 2, 3, 3]
//    Шаг 5:  fast=5, nums[5]=3 != nums[slow]=2 -> новый элемент!
//            slow++ (slow=3), nums[3] = 3.   Массив: [0, 1, 2, 3, 2, 3, 3]
//    Шаг 6:  fast=6, nums[6]=3 == nums[slow]=3 -> дубликат, пропускаем.
//
//    Итог: k = slow + 1 = 4. Первые 4 элемента: [0, 1, 2, 3].
//    Суть: slow отмечает "границу готового результата", fast
//    сканирует весь массив и копирует только уникальные значения.
//
// -----------------------------------------------------
// * 3. Быстрый и медленный (Floyd's):
// -----------------------------------------------------
//    fast делает 2 шага, slow — 1 шаг.
//    Применение: обнаружение циклов, поиск середины списка.
//
//    Пример: обнаружение цикла в связном списке.
//    Список: 1 -> 2 -> 3 -> 4 -> 5 -> 3 (цикл на узел 3)
//
//    Шаг 1: slow=1, fast=2
//    Шаг 2: slow=2, fast=4
//    Шаг 3: slow=3, fast=3 (fast прошёл 5->3)
//            slow == fast -> цикл обнаружен!
//
//    ? Почему это работает? Если цикл есть, fast "догоняет" slow
//    ? внутри цикла, сокращая расстояние на 1 с каждым шагом.
//    ? Если цикла нет — fast дойдёт до конца списка (null).
//    ? Гарантированная встреча за O(n) шагов.
//
// -----------------------------------------------------
// * Когда использовать два указателя?
// -----------------------------------------------------
//    Признаки задачи, подходящей для двух указателей:
//
//    - Массив отсортирован (или можно отсортировать).
//    - Нужно найти пару/тройку элементов с определённым свойством.
//    - Требуется модификация массива in-place без доп. памяти.
//    - В условии упоминается "подмассив", "подстрока", "пара".
//    - Нужно сравнивать элементы с двух концов (палиндромы).
//    - Задача на связный список (цикл, середина, слияние).
//    - Наивное решение O(n^2) и нужно оптимизировать до O(n).
//
// ! Ключевое преимущество: сводит O(n^2) к O(n), потому что
// ! каждый элемент обрабатывается не более одного раза.
//
// ! Типичная сложность: O(n) по времени, O(1) по памяти.
// =====================================================


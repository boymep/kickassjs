// КАК РАБОТАЕТ ТЕХНИКА ДВУХ УКАЗАТЕЛЕЙ
// =====================================================
//
// Идея: вместо перебора всех пар O(n²) используем два указателя,
// которые движутся по массиву/строке по определённой логике.
//
// Три основных варианта:
//
// 1. Сходящиеся указатели (навстречу друг другу):
//    left = 0, right = n-1. Движутся к центру.
//    Применение: задачи на отсортированных массивах,
//    палиндромы, поиск пар с заданным свойством.
//
// 2. Параллельные указатели (в одном направлении):
//    slow и fast оба начинают с 0, но fast идёт быстрее.
//    Применение: удаление дубликатов in-place,
//    разделение массива по условию.
//
// 3. Быстрый и медленный (Floyd's):
//    fast делает 2 шага, slow — 1 шаг.
//    Применение: обнаружение циклов, поиск середины списка.
//
// Ключевое преимущество: сводит O(n²) к O(n), потому что
// каждый элемент обрабатывается не более одного раза.
//
// Типичная сложность: O(n) по времени, O(1) по памяти.
// =====================================================



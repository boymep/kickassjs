function assert(condition, message) {
  if (!condition) {
    console.error("❌ " + message);
  } else {
    console.log("✅ " + message);
  }
}

function assertDeepEqual(a, b, message) {
  assert(
    JSON.stringify(a) === JSON.stringify(b),
    message + " | got: " + JSON.stringify(a) + ", expected: " + JSON.stringify(b),
  );
}

// =====================================================
// * КАК РАБОТАЮТ ХЕШ-ТАБЛИЦЫ В АЛГОРИТМАХ
// =====================================================
//
// Хеш-таблица (в JS — Object или Map) обеспечивает:
// !   - Вставка: O(1) в среднем
// !   - Поиск: O(1) в среднем
// !   - Удаление: O(1) в среднем
//
// * Основные паттерны:
//
// -------------------------------------------------------
// * 1. Частотный подсчёт: пройти массив/строку, подсчитать
//    сколько раз встречается каждый элемент.
// ?    Применение: анаграммы, дубликаты, top-k элементов.
//
//    --- Пример: проверка анаграмм "listen" vs "silent" ---
//
//    Шаг 1. Строим частотную карту для "listen":
//      Символ 'l' -> map = { l:1 }
//      Символ 'i' -> map = { l:1, i:1 }
//      Символ 's' -> map = { l:1, i:1, s:1 }
//      Символ 't' -> map = { l:1, i:1, s:1, t:1 }
//      Символ 'e' -> map = { l:1, i:1, s:1, t:1, e:1 }
//      Символ 'n' -> map = { l:1, i:1, s:1, t:1, e:1, n:1 }
//
//    Шаг 2. Проходим по "silent" и уменьшаем счётчики:
//      's' -> map = { l:1, i:1, s:0, t:1, e:1, n:1 }
//      'i' -> map = { l:1, i:0, s:0, t:1, e:1, n:1 }
//      'l' -> map = { l:0, i:0, s:0, t:1, e:1, n:1 }
//      'e' -> map = { l:0, i:0, s:0, t:1, e:0, n:1 }
//      'n' -> map = { l:0, i:0, s:0, t:1, e:0, n:0 }
//      't' -> map = { l:0, i:0, s:0, t:0, e:0, n:0 }
//
// !    Шаг 3. Все счётчики равны 0 => строки — анаграммы!
// !    Если бы хоть один счётчик != 0, ответ — false.
//
// -------------------------------------------------------
// * 2. Lookup-таблица: сохранить значения для быстрого поиска.
// ?    Применение: two-sum (сохраняем complement), prefix sum.
//
//    --- Пример: twoSum([2, 7, 11, 15], target = 9) ---
//
// ?    Идея: для каждого числа num вычисляем complement = target - num.
// !    Если complement уже есть в Map — нашли пару!
//
//    i=0, num=2:  complement = 9 - 2 = 7.  Map не содержит 7.
//                 Сохраняем: Map = { 2 -> 0 }  (значение -> индекс)
//
//    i=1, num=7:  complement = 9 - 7 = 2.  Map содержит 2! (индекс 0)
//                 Ответ: [0, 1]
//
// !    Итого: за один проход O(n) нашли пару, без вложенных циклов O(n^2).
//
// -------------------------------------------------------
// * 3. Prefix sum + Map: для подмассивов с заданной суммой.
// !    prefixSum[j] - prefixSum[i] = k  =>  ищем prefixSum[i] = prefixSum[j] - k.
// ?    Сохраняем все встреченные prefix sum в Map.
//
//    --- Пример: количество подмассивов с суммой k=3 в [1, 2, 1, -1, 3] ---
//
//    Начальное состояние: prefixSum = 0, Map = { 0: 1 }
//    (Map хранит: значение prefix sum -> сколько раз встречалось)
//
//    i=0, num=1:  prefixSum = 0 + 1 = 1
//                 Ищем prefixSum - k = 1 - 3 = -2 в Map. Нет.
//                 count = 0.  Map = { 0:1, 1:1 }
//
//    i=1, num=2:  prefixSum = 1 + 2 = 3
//                 Ищем prefixSum - k = 3 - 3 = 0 в Map. Есть! (1 раз)
//                 count = 0 + 1 = 1.  Подмассив: [1, 2] (сумма 3)
//                 Map = { 0:1, 1:1, 3:1 }
//
//    i=2, num=1:  prefixSum = 3 + 1 = 4
//                 Ищем prefixSum - k = 4 - 3 = 1 в Map. Есть! (1 раз)
//                 count = 1 + 1 = 2.  Подмассив: [2, 1] (сумма 3)
//                 Map = { 0:1, 1:1, 3:1, 4:1 }
//
//    i=3, num=-1: prefixSum = 4 + (-1) = 3
//                 Ищем prefixSum - k = 3 - 3 = 0 в Map. Есть! (1 раз)
//                 count = 2 + 1 = 3.  Подмассив: [1, 2, 1, -1] (сумма 3)
//                 Map = { 0:1, 1:1, 3:2, 4:1 }
//
//    i=4, num=3:  prefixSum = 3 + 3 = 6
//                 Ищем prefixSum - k = 6 - 3 = 3 в Map. Есть! (2 раза)
//                 count = 3 + 2 = 5.  Подмассивы: [1, -1, 3] и [3]
//                 Map = { 0:1, 1:1, 3:2, 4:1, 6:1 }
//
// !    Ответ: 5 подмассивов с суммой 3.
//
// -------------------------------------------------------
// * 4. Группировка: объединить элементы по общему ключу.
// ?    Ключ может быть строкой, отсортированными символами и т.д.
//
//    --- Пример: groupAnagrams(["eat","tea","tan","ate","nat","bat"]) ---
//
//    Идея: для каждого слова сортируем буквы — получаем ключ.
//    Все анаграммы дадут одинаковый ключ.
//
//    "eat" -> сортируем -> "aet"  => Map = { "aet": ["eat"] }
//    "tea" -> сортируем -> "aet"  => Map = { "aet": ["eat","tea"] }
//    "tan" -> сортируем -> "ant"  => Map = { "aet": ["eat","tea"], "ant": ["tan"] }
//    "ate" -> сортируем -> "aet"  => Map = { "aet": ["eat","tea","ate"], "ant": ["tan"] }
//    "nat" -> сортируем -> "ant"  => Map = { "aet": ["eat","tea","ate"], "ant": ["tan","nat"] }
//    "bat" -> сортируем -> "abt"  => Map = { "aet": ["eat","tea","ate"], "ant": ["tan","nat"], "abt": ["bat"] }
//
//    Результат (values из Map): [["eat","tea","ate"], ["tan","nat"], ["bat"]]
//
// -------------------------------------------------------
// 5. Когда использовать хеш-таблицу?
//
//    Ключевые индикаторы в условии задачи:
//
//    - "Найти пару/тройку элементов с определённым свойством"
//      => lookup-таблица (two-sum, three-sum)
//
//    - "Проверить наличие дубликатов" или "уникальные элементы"
//      => Set или Map для отслеживания уже встреченных
//
//    - "Подсчитать частоту / количество вхождений"
//      => частотная карта (анаграммы, top-k, мажоритарный элемент)
//
//    - "Сгруппировать элементы по признаку"
//      => Map, где ключ — общий признак
//
//    - "Подмассив с заданной суммой / свойством"
//      => prefix sum + Map
//
//    - "Нужно за O(1) проверять, видели ли мы элемент раньше"
//      => Set или Map
//
//    - Задача решается за O(n^2) с двумя циклами, но нужно быстрее
//      => скорее всего, Map заменит внутренний цикл на O(1) поиск
//
//    Красный флаг: если данные не помещаются в память, хеш-таблица
//    не подойдёт. Но на собеседованиях это крайне редкий случай.
//
// -------------------------------------------------------
// Map vs Object в JS:
//   - Map: любые ключи, гарантированный порядок, .size, итерируемый.
//   - Object: только строковые/символьные ключи, удобный синтаксис.
//   Для алгоритмов обычно лучше Map.
//
// Типичная сложность: O(n) по времени, O(n) по памяти.
// =====================================================



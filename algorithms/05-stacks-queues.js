function assert(condition, message) {
  if (!condition) {
    console.error("❌ " + message);
  } else {
    console.log("✅ " + message);
  }
}

function assertDeepEqual(a, b, message) {
  assert(
    JSON.stringify(a) === JSON.stringify(b),
    message + " | got: " + JSON.stringify(a) + ", expected: " + JSON.stringify(b),
  );
}

// =====================================================
// * КАК РАБОТАЮТ СТЕКИ И ОЧЕРЕДИ
// =====================================================
//
// * ----- Стек (Stack) — LIFO (Last In, First Out) -----
//
//   Операции: push (добавить наверх), pop (снять сверху), peek (посмотреть верх).
//   В JS: обычный массив с push() / pop().
// ? Применения: проверка скобок, вычисление выражений,
// ? отмена действий, DFS.
//
//   Пример: проверка валидности скобок в строке "({[]})"
// ? Идея: открывающую скобку кладём в стек, при закрывающей —
// ? проверяем, совпадает ли она с вершиной стека.
//
//   Символ  | Действие                | Стек (снизу -> верх)
//   --------|-------------------------|---------------------
//   '('     | push '('                | ['(']
//   '{'     | push '{'                | ['(', '{']
//   '['     | push '['                | ['(', '{', '[']
//   ']'     | pop '[' — совпадает!    | ['(', '{']
//   '}'     | pop '{' — совпадает!    | ['(']
//   ')'     | pop '(' — совпадает!    | []
//   Конец   | стек пуст => ВАЛИДНО    |
//
//   Если бы строка была "({[})", на символе '}' мы бы сняли '[',
//   а '}' не совпадает с '[' => НЕВАЛИДНО.
//
// * ----- Очередь (Queue) — FIFO (First In, First Out) -----
//
//   Операции: enqueue (добавить в конец), dequeue (забрать из начала).
// ! В JS: массив с push() / shift(). Но shift() — O(n)!
// ? Для производительности: связный список или два стека.
// ? Применения: BFS, планировщики задач.
//
//   Пример: BFS обход дерева по уровням (level-order traversal).
//   Дерево:       1
//               /   \
//              2     3
//             / \     \
//            4   5     6
//
//   Очередь (начало <- конец):
//   1. Начало: очередь = [1]
//   2. Достаём 1, добавляем детей 2, 3 => очередь = [2, 3] => вывод: 1
//   3. Достаём 2, добавляем детей 4, 5 => очередь = [3, 4, 5] => вывод: 2
//   4. Достаём 3, добавляем ребёнка 6  => очередь = [4, 5, 6] => вывод: 3
//   5. Достаём 4 (нет детей)           => очередь = [5, 6]    => вывод: 4
//   6. Достаём 5 (нет детей)           => очередь = [6]       => вывод: 5
//   7. Достаём 6 (нет детей)           => очередь = []        => вывод: 6
//   Результат по уровням: [[1], [2, 3], [4, 5, 6]]
//
// * ----- Монотонный стек -----
//
//   Стек, где элементы всегда упорядочены (возрастающе или убывающе).
//   При добавлении нового элемента все «нарушающие» элементы извлекаются.
// ? Применение: «для каждого элемента найти ближайший больший/меньший».
// ! Сложность: O(n) — каждый элемент входит и выходит из стека
// ! максимум один раз.
//
//   Пример: найти следующий больший элемент для каждого в [2, 1, 4, 3].
//   Используем монотонно убывающий стек (храним индексы).
//   result = [-1, -1, -1, -1] (по умолчанию -1 = "нет большего")
//
//   i=0, num=2 | Стек пуст, push 0          | Стек: [0]      | result: [-1,-1,-1,-1]
//   i=1, num=1 | 1 < nums[0]=2, push 1      | Стек: [0, 1]   | result: [-1,-1,-1,-1]
//              | (1 не больше 2, не выталкиваем)
//   i=2, num=4 | 4 > nums[1]=1 => pop 1,    | Стек: [0]      | result: [-1, 4,-1,-1]
//              |   result[1]=4               |                |
//              | 4 > nums[0]=2 => pop 0,    | Стек: []       | result: [ 4, 4,-1,-1]
//              |   result[0]=4               |                |
//              | Стек пуст, push 2           | Стек: [2]      |
//   i=3, num=3 | 3 < nums[2]=4, push 3      | Стек: [2, 3]   | result: [ 4, 4,-1,-1]
//              | (3 не больше 4, не выталкиваем)
//   Конец:     | Индексы 2, 3 остались в стеке — для них нет большего элемента.
//   Ответ:     | [4, 4, -1, -1]
//
// ! Ключевой принцип: когда текущий элемент больше вершины стека,
// ! мы нашли «следующий больший» для элемента на вершине. Выталкиваем
// ! его и записываем ответ. Повторяем, пока стек не опустеет или
// ! вершина не окажется >= текущего.
//
// * ----- Монотонный дек (deque) -----
//
//   Двусторонняя очередь с монотонностью.
//   Можно добавлять/удалять с обоих концов.
//   Применение: максимум/минимум в скользящем окне за O(n).
//
//   Пример: максимум в скользящем окне размера k=3 для [1, 3, -1, -3, 5].
//   Дек хранит индексы, значения по которым убывают (голова = максимум).
//
//   i=0, num=1  | Дек пуст, push 0           | Дек: [0]        | max: -
//   i=1, num=3  | 3>nums[0]=1, pop 0, push 1 | Дек: [1]        | max: -
//   i=2, num=-1 | -1<nums[1]=3, push 2       | Дек: [1, 2]     | max: nums[1]=3
//               | (окно [1,3,-1] сформировано, голова дека = индекс 1 => max=3)
//   i=3, num=-3 | -3<nums[2]=-1, push 3      | Дек: [1, 2, 3]  | max: nums[1]=3
//               | (окно [3,-1,-3], индекс 1 ещё в окне => max=3)
//   i=4, num=5  | Голова дека=1, но i-k=1, значит индекс 1 вышел за окно => pop слева
//               | 5>nums[3]=-3, pop 3        |                  |
//               | 5>nums[2]=-1, pop 2        |                  |
//               | push 4                     | Дек: [4]         | max: nums[4]=5
//               | (окно [-1,-3,5] => max=5)
//   Ответ:      | [3, 3, 5]
//
// ----- Когда использовать стек? -----
//
//   - Нужно обрабатывать вложенные структуры (скобки, HTML-теги, рекурсия)
//   - Задача связана с «откатом» или «отменой» (undo)
//   - Нужно обрабатывать элементы в обратном порядке
//   - Задача вида «найти ближайший больший/меньший» => монотонный стек
//   - Вычисление выражений (инфиксных, постфиксных)
//   - DFS (обход в глубину) — итеративная реализация через стек
//   - Ключевое слово: «последний незакрытый», «последний необработанный»
//
// ----- Когда использовать очередь? -----
//
//   - BFS (обход в ширину) — поуровневый обход графа/дерева
//   - Обработка в порядке поступления (FIFO)
//   - Скользящее окно с поиском min/max => монотонный дек
//   - Планировщики задач, буферы, потоки данных
//   - Ключевое слово: «по уровням», «в порядке поступления»,
//     «первый пришёл — первый обслужен»
//
// =====================================================



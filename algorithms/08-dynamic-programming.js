function assert(condition, message) {
  if (!condition) {
    console.error("❌ " + message);
  } else {
    console.log("✅ " + message);
  }
}

function assertDeepEqual(a, b, message) {
  assert(
    JSON.stringify(a) === JSON.stringify(b),
    message + " | got: " + JSON.stringify(a) + ", expected: " + JSON.stringify(b),
  );
}

// =====================================================
// * КАК РАБОТАЕТ ДИНАМИЧЕСКОЕ ПРОГРАММИРОВАНИЕ (DP)
// =====================================================
//
// ! DP — метод решения задач, которые можно разбить на
// ! перекрывающиеся подзадачи. Вместо повторного вычисления
// ! одних и тех же подзадач, мы сохраняем их результаты.
//
// Два подхода:
//
// * 1. Top-down (сверху вниз) — рекурсия + мемоизация:
//    function solve(n, memo = {}) {
//      if (n in memo) return memo[n];
//      // ... вычисляем через solve(n-1), solve(n-2) и т.д.
//      memo[n] = result;
//      return result;
//    }
//
// --- Пример: Fibonacci(5) с мемоизацией (Top-down) ---
//
// ? Без мемоизации дерево вызовов содержит повторы:
//
//                    fib(5)
//                   /      \
//              fib(4)       fib(3)       <-- fib(3) считается дважды!
//             /     \       /    \
//         fib(3)  fib(2) fib(2) fib(1)   <-- fib(2) считается трижды!
//        /    \
//    fib(2) fib(1)
//
// ! С мемоизацией каждое значение вычисляется только один раз:
//
//   Вызов fib(5):
//     memo = {}
//     -> вызов fib(4):
//          memo = {}
//          -> вызов fib(3):
//               memo = {}
//               -> вызов fib(2):
//                    memo = {}
//                    -> fib(1) = 1 (базовый случай)
//                    -> fib(0) = 0 (базовый случай)
//                    -> memo[2] = 1, возвращаем 1
//               -> вызов fib(1) = 1 (базовый случай)
//               -> memo[3] = 1 + 1 = 2, возвращаем 2
//          -> вызов fib(2): НАЙДЕНО в memo! memo[2] = 1, возвращаем 1
//          -> memo[4] = 2 + 1 = 3, возвращаем 3
//     -> вызов fib(3): НАЙДЕНО в memo! memo[3] = 2, возвращаем 2
//     -> memo[5] = 3 + 2 = 5, возвращаем 5
//
//   Итоговое состояние memo: { 2: 1, 3: 2, 4: 3, 5: 5 }
//   ! Без мемоизации: 15 вызовов. С мемоизацией: 9 вызовов.
//
// * 2. Bottom-up (снизу вверх) — таблица (итеративно):
//    const dp = new Array(n + 1);
//    dp[0] = базовый случай;
//    for (let i = 1; i <= n; i++) {
//      dp[i] = f(dp[i-1], dp[i-2], ...);
//    }
//
// --- Пример: Fibonacci(5) снизу вверх (Bottom-up) ---
//
//   Заполняем массив dp слева направо:
//
//   Шаг 0: dp[0] = 0                    dp = [0]
//   Шаг 1: dp[1] = 1                    dp = [0, 1]
//   Шаг 2: dp[2] = dp[1] + dp[0]       dp = [0, 1, 1]
//          = 1 + 0 = 1
//   Шаг 3: dp[3] = dp[2] + dp[1]       dp = [0, 1, 1, 2]
//          = 1 + 1 = 2
//   Шаг 4: dp[4] = dp[3] + dp[2]       dp = [0, 1, 1, 2, 3]
//          = 2 + 1 = 3
//   Шаг 5: dp[5] = dp[4] + dp[3]       dp = [0, 1, 1, 2, 3, 5]
//          = 3 + 2 = 5
//
//   Ответ: dp[5] = 5
//   ! Время: O(n), Память: O(n), но можно O(1) — храним только prev и curr.
//
// Как решать DP задачу (5 шагов):
//   1. Определить состояние — что является подзадачей? (dp[i] = ?)
//   2. Найти рекуррентное соотношение — как dp[i] выражается через dp[j]?
//   3. Определить базовые случаи — dp[0], dp[1], ...
//   4. Определить порядок вычисления — слева направо, построчно и т.д.
//   5. (Опционально) Оптимизировать память — часто нужны только
//      последние 1-2 значения, а не вся таблица.
//
// --- Пример: применяем 5 шагов к "Minimum Cost Climbing Stairs" ---
//
//   Задача: дан массив cost, где cost[i] — стоимость шага со ступени i.
//   Можно шагать на 1 или 2 ступени. Найти мин. стоимость добраться
//   до вершины (за последнюю ступень). Вход: cost = [10, 15, 20].
//
//   Шаг 1. Состояние:
//     dp[i] = минимальная стоимость, чтобы ВСТАТЬ на ступень i.
//
//   Шаг 2. Рекуррентное соотношение:
//     На ступень i можно прийти с i-1 или с i-2, значит:
//     dp[i] = cost[i] + min(dp[i-1], dp[i-2])
//
//   Шаг 3. Базовые случаи:
//     dp[0] = cost[0] = 10  (встать на ступень 0 стоит 10)
//     dp[1] = cost[1] = 15  (встать на ступень 1 стоит 15, можно начать сразу с неё)
//
//   Шаг 4. Порядок вычисления — слева направо:
//     dp[0] = 10
//     dp[1] = 15
//     dp[2] = cost[2] + min(dp[1], dp[0])
//           = 20 + min(15, 10)
//           = 20 + 10 = 30
//     Ответ: min(dp[1], dp[2]) = min(15, 30) = 15
//     (с вершины ступени 1 можно перешагнуть сразу на верх)
//
//   Шаг 5. Оптимизация памяти:
//     dp[i] зависит только от dp[i-1] и dp[i-2], значит вместо
//     массива можно хранить две переменные prev и curr:
//     let prev = cost[0], curr = cost[1];
//     for (let i = 2; i < cost.length; i++) {
//       let next = cost[i] + Math.min(prev, curr);
//       prev = curr;
//       curr = next;
//     }
//     return Math.min(prev, curr);
//     Память: O(1) вместо O(n).
//
// Признаки DP задачи:
//   - "Сколько способов...", "Минимальная стоимость...",
//     "Возможно ли...", "Самая длинная..."
//   - Есть оптимальная подструктура и перекрывающиеся подзадачи.
//
// Сложность: зависит от количества состояний x стоимость перехода.
//
// =====================================================
// ТИПИЧНЫЕ ПАТТЕРНЫ DP
// =====================================================
//
// 1. Линейное DP (Linear DP)
//    dp[i] зависит от предыдущих элементов dp[i-1], dp[i-2], ...
//    Примеры: Fibonacci, Climbing Stairs, House Robber, Maximum Subarray.
//    Время: обычно O(n) или O(n^2).
//
// 2. DP на сетке (2D Grid DP)
//    dp[i][j] зависит от соседних ячеек: dp[i-1][j], dp[i][j-1] и т.д.
//    Примеры: Minimum Path Sum, Unique Paths, Maximal Square.
//    Время: O(m * n).
//
// 3. Рюкзак (Knapsack)
//    dp[i][w] — оптимальное значение при i предметах и ограничении w.
//    Варианты: 0/1 knapsack, unbounded knapsack, subset sum.
//    Примеры: Coin Change, Partition Equal Subset Sum, Target Sum.
//    Время: O(n * W), где W — ограничение (сумма/вес).
//
// 4. Строковое DP (String DP)
//    dp[i][j] — результат для подстрок s[0..i] и t[0..j].
//    Примеры: Longest Common Subsequence (LCS), Edit Distance,
//    Longest Palindromic Subsequence, Word Break.
//    Время: обычно O(n * m), где n и m — длины строк.
//
// Совет для собеседования: определите паттерн задачи — это
// сразу подскажет структуру состояния и рекуррентного соотношения.
// =====================================================



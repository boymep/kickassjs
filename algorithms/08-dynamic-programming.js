function assert(condition, message) {
  if (!condition) {
    console.error("❌ " + message);
  } else {
    console.log("✅ " + message);
  }
}

function assertDeepEqual(a, b, message) {
  assert(
    JSON.stringify(a) === JSON.stringify(b),
    message + " | got: " + JSON.stringify(a) + ", expected: " + JSON.stringify(b),
  );
}

// =====================================================
// КАК РАБОТАЕТ ДИНАМИЧЕСКОЕ ПРОГРАММИРОВАНИЕ (DP)
// =====================================================
//
// DP — метод решения задач, которые можно разбить на
// перекрывающиеся подзадачи. Вместо повторного вычисления
// одних и тех же подзадач, мы сохраняем их результаты.
//
// Два подхода:
//
// 1. Top-down (сверху вниз) — рекурсия + мемоизация:
//    function solve(n, memo = {}) {
//      if (n in memo) return memo[n];
//      // ... вычисляем через solve(n-1), solve(n-2) и т.д.
//      memo[n] = result;
//      return result;
//    }
//
// 2. Bottom-up (снизу вверх) — таблица (итеративно):
//    const dp = new Array(n + 1);
//    dp[0] = базовый случай;
//    for (let i = 1; i <= n; i++) {
//      dp[i] = f(dp[i-1], dp[i-2], ...);
//    }
//
// Как решать DP задачу:
//   1. Определить состояние — что является подзадачей? (dp[i] = ?)
//   2. Найти рекуррентное соотношение — как dp[i] выражается через dp[j]?
//   3. Определить базовые случаи — dp[0], dp[1], ...
//   4. Определить порядок вычисления — слева направо, построчно и т.д.
//   5. (Опционально) Оптимизировать память — часто нужны только
//      последние 1-2 значения, а не вся таблица.
//
// Признаки DP задачи:
//   - "Сколько способов...", "Минимальная стоимость...",
//     "Возможно ли...", "Самая длинная..."
//   - Есть оптимальная подструктура и перекрывающиеся подзадачи.
//
// Сложность: зависит от количества состояний × стоимость перехода.
// =====================================================



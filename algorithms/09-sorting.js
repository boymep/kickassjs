//
// Сравнительные сортировки (основаны на сравнении элементов):
//
// 1. Сортировка пузырьком (Bubble Sort):
//    Повторно проходим массив, меняя соседние элементы.
//    O(n²) по времени, O(1) по памяти. Стабильная.
//    На практике — медленная, но простая.
//
// 2. Сортировка слиянием (Merge Sort):
//    Divide & Conquer: делим массив пополам, сортируем рекурсивно,
//    сливаем два отсортированных массива.
//    O(n log n) по времени, O(n) по памяти. Стабильная.
//    Гарантированная O(n log n) в любом случае.
//
// 3. Быстрая сортировка (Quick Sort):
//    Выбираем опорный элемент (pivot), разбиваем массив на
//    «меньше pivot» и «больше pivot», сортируем рекурсивно.
//    O(n log n) в среднем, O(n²) в худшем. O(log n) по памяти.
//    Нестабильная, но на практике обычно быстрее merge sort.
//
// 4. Quickselect:
//    Модификация quick sort для поиска k-го элемента
//    без полной сортировки. O(n) в среднем.
//
// Нижняя граница для сравнительных сортировок: O(n log n).
// Это нельзя улучшить для произвольных данных!
//
// Стабильность: стабильная сортировка сохраняет относительный
// порядок элементов с одинаковым ключом.
// =====================================================



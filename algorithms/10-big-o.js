// ===== ФУНКЦИЯ 5: Фибоначчи — наивная рекурсия =====
function fibNaive(n) {
  if (n <= 1) return n;
  return fibNaive(n - 1) + fibNaive(n - 2);
}

// Определите:
// 1. Временная сложность: O(?)
// 2. Пространственная сложность: O(?)
// 3. Объяснение: ???

// --- Ответ 5 ---
// // Временная сложность: O(2ⁿ)
// // Пространственная сложность: O(n)
// // Объяснение: каждый вызов порождает два новых → дерево вызовов
// // удваивается на каждом уровне. Всего ~2ⁿ вызовов.
// // Пространство = глубина стека вызовов = n (ветка идёт вниз до 0).
// // Это ОЧЕНЬ медленно! fib(40) уже ощутимо тормозит.


// ===== ФУНКЦИЯ 6: Фибоначчи с мемоизацией =====
function fibMemo(n, memo = {}) {
  if (n <= 1) return n;
  if (memo[n] !== undefined) return memo[n];
  memo[n] = fibMemo(n - 1, memo) + fibMemo(n - 2, memo);
  return memo[n];
}


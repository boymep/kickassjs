// 1. Временная сложность: O(?)
// 2. Пространственная сложность: O(?)
// 3. Объяснение: ???

// --- Ответ 14 ---
// // Временная сложность: O(n × m)
// // Пространственная сложность: O(min(n, m)) — в худшем случае
// // Объяснение: цикл выполняется n раз. На каждой итерации

// =====================================================
// * КРАТКАЯ СПРАВКА ПО BIG-O
// =====================================================
//
// Big-O описывает верхнюю границу роста функции при увеличении
// размера входных данных n → ∞. Константы и младшие члены отбрасываются.
//
// * Основные классы (от быстрого к медленному):
//   O(1)       — константная: не зависит от n
//   O(log n)   — логарифмическая: деление пополам на каждом шаге
//   O(n)       — линейная: один проход по данным
//   O(n log n) — линейно-логарифмическая: эффективные сортировки
//   O(n²)      — квадратичная: вложенные циклы
//   O(2ⁿ)      — экспоненциальная: полный перебор подмножеств
//   O(n!)      — факториальная: полный перебор перестановок
//
// * Правила анализа:
//   1. Последовательные блоки: O(a) + O(b) = O(max(a, b))
//   2. Вложенные циклы: O(a) × O(b) = O(a × b)
//   3. Рекурсия: глубина × работа на каждом уровне
//   4. Пространство: доп. структуры + стек вызовов (для рекурсии)
//
// * Подвохи в JavaScript:
//   ! str += char → создаёт новую строку на каждой итерации → O(n) за операцию!
//   - Array.includes(), indexOf() → линейный поиск O(n)
//   - Array.shift() → сдвиг всех элементов O(n)
//   - Array.sort() → O(n log n)
//   - Object.keys() → O(n)
//   - Set.has(), Map.get() → O(1)
// =====================================================



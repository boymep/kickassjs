// --- Ответ 8 ---
// // Временная сложность: O(n log n)
// // Пространственная сложность: O(n log n)
// // Объяснение: дерево рекурсии имеет log₂(n) уровней.
// // На каждом уровне суммарно обрабатывается n элементов (слияние).
// // Итого: n × log n.
// // Память: на каждом уровне рекурсии создаются новые массивы через slice().
// // slice() создаёт копию → O(n) на уровень × log n уровней = O(n log n).
// // (Можно оптимизировать до O(n) доп. памяти с in-place подходом.)



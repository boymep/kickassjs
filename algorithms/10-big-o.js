// // Временная сложность: O(log n)
// // Пространственная сложность: O(log n)
// // Объяснение: на каждом шаге пространство поиска делится пополам →
// // максимум log₂(n) рекурсивных вызовов.
// // В отличие от итеративного варианта (O(1) по памяти),
// // рекурсивный занимает O(log n) из-за стека вызовов!


// ===== ФУНКЦИЯ 8: Сортировка слиянием =====
function mergeSortAnalysis(arr) {
  if (arr.length <= 1) return arr;
  const mid = Math.floor(arr.length / 2);
  const left = mergeSortAnalysis(arr.slice(0, mid));
  const right = mergeSortAnalysis(arr.slice(mid));
  const result = [];
  let i = 0, j = 0;
  while (i < left.length && j < right.length) {
    if (left[i] <= right[j]) result.push(left[i++]);
    else result.push(right[j++]);
  }
  while (i < left.length) result.push(left[i++]);
  while (j < right.length) result.push(right[j++]);
  return result;
}


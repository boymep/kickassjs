// --- Ответ 13 ---
// // Временная сложность: O(n²)  ← ПОДВОХ!
// // Пространственная сложность: O(n)
// // Объяснение: строки в JavaScript ИММУТАБЕЛЬНЫ.
// // Каждое str += "a" создаёт НОВУЮ строку и копирует все
// // предыдущие символы. На шаге i копируется i символов.
// // Итого: 1 + 2 + 3 + ... + n = n(n+1)/2 = O(n²).
// //
// // Правильный подход: собирать в массив и сделать arr.join(""):

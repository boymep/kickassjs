// ===== ФУНКЦИЯ 4: Два последовательных цикла =====
function processTwo(arr1, arr2) {
  let sum1 = 0;
  for (let i = 0; i < arr1.length; i++) {
    sum1 += arr1[i];
  }
  let sum2 = 0;
  for (let j = 0; j < arr2.length; j++) {
    sum2 += arr2[j];
  }
  return sum1 + sum2;
}


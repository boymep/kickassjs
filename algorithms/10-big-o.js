// ===== ФУНКЦИЯ 13: Конкатенация строк в цикле =====
function buildString(n) {
  let str = "";
  for (let i = 0; i < n; i++) {
    str += "a";  // ← ЭТА СТРОКА — ЛОВУШКА!
  }
  return str;
}


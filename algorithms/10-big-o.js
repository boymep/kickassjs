// // Пространственная сложность: O(n)
// // Объяснение: каждое значение fib(k) вычисляется только один раз
// // и сохраняется в memo. Всего n уникальных подзадач → O(n) вызовов.
// // Пространство: memo хранит n значений + стек вызовов глубины n.


// ===== ФУНКЦИЯ 7: Бинарный поиск — рекурсивный =====
function binarySearchRec(arr, target, left = 0, right = arr.length - 1) {
  if (left > right) return -1;
  const mid = Math.floor((left + right) / 2);
  if (arr[mid] === target) return mid;
  if (arr[mid] < target) return binarySearchRec(arr, target, mid + 1, right);
  return binarySearchRec(arr, target, left, mid - 1);
}


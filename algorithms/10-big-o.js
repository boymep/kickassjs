// ===== ФУНКЦИЯ 10: Проверка дубликатов через Set =====
function hasDuplicates(arr) {
  const seen = new Set();
  for (const item of arr) {
    if (seen.has(item)) return true;
    seen.add(item);
  }
  return false;
}


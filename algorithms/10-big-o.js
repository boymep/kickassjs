// ===== ФУНКЦИЯ 9: Построение частотной карты =====
function buildFrequencyMap(arr) {
  const map = {};
  for (let i = 0; i < arr.length; i++) {
    const key = arr[i];
    map[key] = (map[key] || 0) + 1;
  }
  return map;
}


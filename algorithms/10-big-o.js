// ===== ФУНКЦИЯ 12: Создание матрицы n×n =====
function createMatrix(n) {
  const matrix = [];
  for (let i = 0; i < n; i++) {
    const row = [];
    for (let j = 0; j < n; j++) {
      row.push(i * n + j);
    }
    matrix.push(row);
  }
  return matrix;
}

